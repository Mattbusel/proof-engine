// ── JournalNote ───────────────────────────────────────────────────────────────

/// A timestamped note attached to a quest in the journal.
#[derive(Debug, Clone)]
pub struct JournalNote {
    /// Game-world time when the note was written.
    pub timestamp: f32,
    /// Note text.
    pub text: String,
    /// `true` when generated by the engine rather than the player.
    pub auto_generated: bool,
}

impl JournalNote {
    pub fn new(timestamp: f32, text: impl Into<String>) -> Self {
        Self { timestamp, text: text.into(), auto_generated: false }
    }

    pub fn auto(timestamp: f32, text: impl Into<String>) -> Self {
        Self { timestamp, text: text.into(), auto_generated: true }
    }
}

// ── ObjectiveProgress ─────────────────────────────────────────────────────────

/// Runtime progress for a single objective.
#[derive(Debug, Clone)]
pub struct ObjectiveProgress {
    /// The definition this progress corresponds to.
    pub def_id: ObjectiveId,
    /// Current progress counter.
    pub current: u32,
    /// Current state.
    pub state: ObjectiveState,
    /// Game-world time when the objective was marked complete.
    pub completed_at: Option<f32>,
}

impl ObjectiveProgress {
    fn new(def_id: ObjectiveId) -> Self {
        Self { def_id, current: 0, state: ObjectiveState::Active, completed_at: None }
    }

    /// Fraction completed [0.0, 1.0].  Returns 1.0 if target_count is 0.
    pub fn fraction(&self, target: u32) -> f32 {
        if target == 0 { return 1.0; }
        (self.current as f32 / target as f32).min(1.0)
    }

    pub fn is_done(&self) -> bool { self.state.is_done() }
}

// ── QuestInstanceProgress ─────────────────────────────────────────────────────

/// Runtime progress for an entire quest (distinct from `QuestProgress` view
/// used in prerequisite checks — see `mod.rs`).
#[derive(Debug, Clone)]
pub struct QuestProgress {
    /// The quest definition id.
    pub def_id: QuestId,
    /// Current lifecycle state.
    pub state: QuestState,
    /// Progress per objective, keyed by `ObjectiveId`.
    pub objectives: HashMap<ObjectiveId, ObjectiveProgress>,
    /// Game-world time when the quest was accepted.
    pub started_at: Option<f32>,
    /// Game-world time when the quest reached a terminal state.
    pub completed_at: Option<f32>,
    /// Total seconds elapsed while quest was in `Active` state.
    pub time_elapsed: f32,
    /// How many times this quest has been attempted (including current).
    pub attempt_count: u32,
    /// Player / auto-generated notes.
    pub notes: Vec<JournalNote>,
}

impl QuestProgress {
    fn new(def: &QuestDef, time: f32) -> Self {
        let mut objectives = HashMap::new();
        for obj in &def.objectives {
            objectives.insert(obj.id, ObjectiveProgress::new(obj.id));
        }
        Self {
            def_id: def.id,
            state: QuestState::Active,
            objectives,
            started_at: Some(time),
            completed_at: None,
            time_elapsed: 0.0,
            attempt_count: 1,
            notes: Vec::new(),
        }
    }

    /// How many required objectives have been completed.
    pub fn required_completed(&self, def: &QuestDef) -> usize {
        def.objectives
            .iter()
            .filter(|o| !o.optional)
            .filter(|o| {
                self.objectives
                    .get(&o.id)
                    .map_or(false, |p| p.state == ObjectiveState::Completed)
            })
            .count()
    }

    /// How many required objectives exist in total.
    pub fn required_total(&self, def: &QuestDef) -> usize {
        def.objectives.iter().filter(|o| !o.optional).count()
    }

    /// Whether all required objectives are complete.
    pub fn all_required_complete(&self, def: &QuestDef) -> bool {
        self.required_completed(def) == self.required_total(def)
    }

    /// Overall completion fraction [0.0, 1.0] based on required objectives.
    pub fn completion_fraction(&self, def: &QuestDef) -> f32 {
        let total = self.required_total(def);
        if total == 0 { return 1.0; }
        self.required_completed(def) as f32 / total as f32
    }

    /// Whether the time limit has been exceeded.
    pub fn is_timed_out(&self, def: &QuestDef) -> bool {
        if let Some(limit) = def.time_limit {
            self.time_elapsed >= limit
        } else {
            false
        }
    }
}

