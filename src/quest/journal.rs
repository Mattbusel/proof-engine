//! # Quest Journal
//!
//! Tracks in-progress and completed quests, manages objective advancement,
//! applies time limits, fires quest events, and maintains world flags.
//!
//! The journal is the single source of truth for quest state. The tracker
//! module routes game events into journal method calls.

