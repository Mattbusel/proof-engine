//! Classic marching cubes implementation with full 256-entry lookup tables.
//!
//! Extracts a triangle mesh from the entity's scalar field at the isosurface threshold.
//! Vertex positions are interpolated along edges, normals from field gradient, and
//! color/emission from weighted source contributions.

use glam::{Vec2, Vec3, Vec4};
use super::entity_field::MetaballEntity;
use super::field_eval::{FieldEvaluator, FieldGrid};

// ── Output types ────────────────────────────────────────────────────────────

/// A vertex produced by marching cubes extraction.
#[derive(Debug, Clone, Copy)]
pub struct MCVertex {
    pub position: Vec3,
    pub normal: Vec3,
    pub color: Vec4,
    pub emission: f32,
}

/// The extracted mesh from marching cubes.
#[derive(Debug, Clone)]
pub struct ExtractedMesh {
    pub vertices: Vec<MCVertex>,
    pub indices: Vec<u32>,
    pub vertex_count: u32,
    pub triangle_count: u32,
}

impl ExtractedMesh {
    pub fn new() -> Self {
        Self { vertices: Vec::new(), indices: Vec::new(), vertex_count: 0, triangle_count: 0 }
    }

    pub fn is_empty(&self) -> bool { self.vertices.is_empty() }

    /// Memory usage in bytes.
    pub fn memory_bytes(&self) -> usize {
        self.vertices.len() * std::mem::size_of::<MCVertex>()
            + self.indices.len() * 4
    }
}

impl Default for ExtractedMesh {
    fn default() -> Self { Self::new() }
}

// ── Extractor ───────────────────────────────────────────────────────────────

/// Marching cubes mesh extractor.
pub struct MarchingCubesExtractor {
    pub evaluator: FieldEvaluator,
}

impl MarchingCubesExtractor {
    pub fn new() -> Self {
        Self { evaluator: FieldEvaluator::full() }
    }

    pub fn fast() -> Self {
        Self { evaluator: FieldEvaluator::with_gradient() }
    }

    /// Extract the isosurface mesh from the entity.
    pub fn extract(&self, entity: &MetaballEntity) -> ExtractedMesh {
        let (bounds_min, bounds_max) = entity.bounds();
        let res = entity.grid_resolution;

        // Sample the field on a grid
        let grid = self.evaluator.sample_grid(entity, bounds_min, bounds_max, res);

        // March through all cells
        self.march(&grid, entity.threshold)
    }

    /// Extract from a pre-computed grid.
    pub fn march(&self, grid: &FieldGrid, threshold: f32) -> ExtractedMesh {
        let mut mesh = ExtractedMesh::new();
        let res = grid.resolution as usize;
        if res < 2 { return mesh; }

        for z in 0..res - 1 {
            for y in 0..res - 1 {
                for x in 0..res - 1 {
                    self.process_cell(&mut mesh, grid, x, y, z, threshold);
                }
            }
        }

        mesh.vertex_count = mesh.vertices.len() as u32;
        mesh.triangle_count = mesh.indices.len() as u32 / 3;
        mesh
    }

    fn process_cell(
        &self,
        mesh: &mut ExtractedMesh,
        grid: &FieldGrid,
        x: usize, y: usize, z: usize,
        threshold: f32,
    ) {
        // Sample 8 corners
        let corners = [
            (x,   y,   z  ), (x+1, y,   z  ), (x+1, y+1, z  ), (x,   y+1, z  ),
            (x,   y,   z+1), (x+1, y,   z+1), (x+1, y+1, z+1), (x,   y+1, z+1),
        ];
        let values: [f32; 8] = std::array::from_fn(|i| grid.strength_at(corners[i].0, corners[i].1, corners[i].2));
        let positions: [Vec3; 8] = std::array::from_fn(|i| grid.position_at(corners[i].0, corners[i].1, corners[i].2));
        let colors: [Vec4; 8] = std::array::from_fn(|i| grid.color_at(corners[i].0, corners[i].1, corners[i].2));
        let emissions: [f32; 8] = std::array::from_fn(|i| grid.emission_at(corners[i].0, corners[i].1, corners[i].2));

        // Classify cell
        let mut cube_index = 0u8;
        for i in 0..8 {
            if values[i] >= threshold { cube_index |= 1 << i; }
        }

        // Early out: fully inside or fully outside
        if cube_index == 0 || cube_index == 0xFF { return; }

        let edge_mask = EDGE_TABLE[cube_index as usize];
        if edge_mask == 0 { return; }

        // Interpolate vertices along intersected edges
        let edges: [(usize, usize); 12] = [
            (0,1),(1,2),(2,3),(3,0),(4,5),(5,6),(6,7),(7,4),(0,4),(1,5),(2,6),(3,7)
        ];

        let mut edge_verts: [MCVertex; 12] = [MCVertex {
            position: Vec3::ZERO, normal: Vec3::Y, color: Vec4::ONE, emission: 0.0,
        }; 12];

        for i in 0..12 {
            if (edge_mask & (1 << i)) == 0 { continue; }
            let (a, b) = edges[i];
            let t = if (values[b] - values[a]).abs() < 1e-8 {
                0.5
            } else {
                (threshold - values[a]) / (values[b] - values[a])
            };
            let t = t.clamp(0.0, 1.0);

            let pos = positions[a].lerp(positions[b], t);
            let color = colors[a].lerp(colors[b], t);
            let emission = emissions[a] + (emissions[b] - emissions[a]) * t;

            // Normal from gradient of the field values (approximate from grid)
            let normal = compute_grid_normal(grid, pos);

            edge_verts[i] = MCVertex { position: pos, normal, color, emission };
        }

        // Generate triangles
        let row = &TRI_TABLE[cube_index as usize];
        let mut i = 0;
        while i < 16 && row[i] != -1 {
            let base = mesh.vertices.len() as u32;
            mesh.vertices.push(edge_verts[row[i] as usize]);
            mesh.vertices.push(edge_verts[row[i + 1] as usize]);
            mesh.vertices.push(edge_verts[row[i + 2] as usize]);
            mesh.indices.push(base);
            mesh.indices.push(base + 1);
            mesh.indices.push(base + 2);
            i += 3;
        }
    }
}

impl Default for MarchingCubesExtractor {
    fn default() -> Self { Self::new() }
}

/// Compute normal at a point from grid values via central differences.
fn compute_grid_normal(grid: &FieldGrid, point: Vec3) -> Vec3 {
    let step = grid.step;
    let res = grid.resolution as usize;

    // Find grid cell containing this point
    let rel = (point - grid.bounds_min) / step;
    let xi = (rel.x as usize).clamp(1, res.saturating_sub(2));
    let yi = (rel.y as usize).clamp(1, res.saturating_sub(2));
    let zi = (rel.z as usize).clamp(1, res.saturating_sub(2));

    let dx = grid.strength_at(xi + 1, yi, zi) - grid.strength_at(xi.saturating_sub(1), yi, zi);
    let dy = grid.strength_at(xi, yi + 1, zi) - grid.strength_at(xi, yi.saturating_sub(1), zi);
    let dz = grid.strength_at(xi, yi, zi + 1) - grid.strength_at(xi, yi, zi.saturating_sub(1));

    let normal = -Vec3::new(dx, dy, dz); // negative gradient points outward
    normal.normalize_or_zero()
}

