#[cfg(test)]
mod tests {
    use super::*;
    use super::super::entity_field::FieldSource;

    fn sphere_entity() -> MetaballEntity {
        let mut e = MetaballEntity::new(0.5, 16);
        e.add_source(FieldSource::new(Vec3::ZERO, 1.0, 1.5));
        e
    }

    #[test]
    fn extract_sphere_produces_mesh() {
        let e = sphere_entity();
        let extractor = MarchingCubesExtractor::new();
        let mesh = extractor.extract(&e);
        assert!(!mesh.is_empty(), "Should extract vertices from a sphere metaball");
        assert!(mesh.vertex_count > 10, "vert_count={}", mesh.vertex_count);
        assert!(mesh.triangle_count > 3, "tri_count={}", mesh.triangle_count);
    }

    #[test]
    fn damaged_entity_produces_smaller_mesh() {
        let mut full = sphere_entity();
        let extractor = MarchingCubesExtractor::new();
        let mesh_full = extractor.extract(&full);

        full.set_hp(0.3);
        let mesh_damaged = extractor.extract(&full);
        // Damaged entity should have fewer or similar vertices (weaker field → smaller surface)
        assert!(mesh_damaged.vertex_count <= mesh_full.vertex_count + 50,
            "Damaged should be <= full: {} vs {}", mesh_damaged.vertex_count, mesh_full.vertex_count);
    }

    #[test]
    fn dead_entity_produces_empty_mesh() {
        let mut e = sphere_entity();
        e.set_hp(0.0);
        let extractor = MarchingCubesExtractor::new();
        let mesh = extractor.extract(&e);
        assert!(mesh.is_empty(), "Dead entity should produce no mesh");
    }

    #[test]
    fn edge_table_has_correct_symmetries() {
        // Config 0 = all outside, config 255 = all inside: both should have no edges
        assert_eq!(EDGE_TABLE[0], 0);
        assert_eq!(EDGE_TABLE[255], 0);
    }

    #[test]
    fn tri_table_terminates() {
        for (i, row) in TRI_TABLE.iter().enumerate() {
            // Each row should either start with -1 or contain valid edge indices (0..11)
            if row[0] != -1 {
                let mut j = 0;
                while j < 16 && row[j] != -1 {
                    assert!(row[j] >= 0 && row[j] <= 11, "Invalid edge index {} in config {}", row[j], i);
                    j += 1;
                }
                // Should be a multiple of 3 before termination
                assert!(j % 3 == 0, "Config {} has {} edge refs (not multiple of 3)", i, j);
            }
        }
    }
}
